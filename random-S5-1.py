def suma(a, b):
    return a + b
    
def resta(a, b):
    return a - b
    
def multiplicacion(a, b):
    return a * b
    
def division(a, b):
    return a / b
    

print(suma(4, 5))

print('-------------')

print(resta(84, 24))

print('-------------')

print(multiplicacion(9, 9))

print('-------------')

print(division(95, 5))


